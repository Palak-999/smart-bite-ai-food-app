function makeReviews(names: string[]): Food['reviews'] {
  const comments = [
    'Absolutely delicious, will order again!',
    'Great portion size and arrived hot.',
    'Packaging was neat and the taste was on point.',
    'A little pricey but worth every bite.',
    'My new favorite comfort food.',
  ]
  return names.map((n, i) => ({
    id: `rev-${n}-${i}`,
    name: n,
    rating: 4 + (i % 2 === 0 ? 1 : 0),
    date: ['2 days ago', '1 week ago', '3 weeks ago'][i % 3],
    comment: comments[i % comments.length],
  }))
}

