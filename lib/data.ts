export function getRestaurantBySlug(slug: string) {
  return restaurants.find((r) => r.slug === slug)
}

export function getFoodBySlug(slug: string) {
  return foods.find((f) => f.slug === slug)
}

export function getFoodsByRestaurant(restaurantId: string) {
  return foods.filter((f) => f.restaurantId === restaurantId)
}

export function getRecommendedFoods(excludeId: string, limit = 6) {
  return foods.filter((f) => f.id !== excludeId).slice(0, limit)
}
